#ifndef COLORS_HPP
#define COLORS_HPP

// Reset
#define RESET   "\033[0m"

// Regular Colors
#define BLACK   "\033[30m"      // Black
#define RED     "\033[31m"      // Red
#define GREEN   "\033[32m"      // Green
#define YELLOW  "\033[33m"      // Yellow
#define BLUE    "\033[34m"      // Blue
#define MAGENTA "\033[35m"      // Magenta
#define CYAN    "\033[36m"      // Cyan
#define WHITE   "\033[37m"      // White
#define GREY    "\033[90m"      // Grey

// Bold
#define BBLACK   "\033[1m\033[30m"      // Black
#define BRED     "\033[1m\033[31m"      // Red
#define BGREEN   "\033[1m\033[32m"      // Green
#define BYELLOW  "\033[1m\033[33m"      // Yellow
#define BBLUE    "\033[1m\033[34m"      // Blue
#define BMAGENTA "\033[1m\033[35m"      // Magenta
#define BCYAN    "\033[1m\033[36m"      // Cyan
#define BWHITE   "\033[1m\033[37m"      // White

// Underline
#define UBLACK   "\033[4m\033[30m"      // Black
#define URED     "\033[4m\033[31m"      // Red
#define UGREEN   "\033[4m\033[32m"      // Green
#define UYELLOW  "\033[4m\033[33m"      // Yellow
#define UBLUE    "\033[4m\033[34m"      // Blue
#define UMAGENTA "\033[4m\033[35m"      // Magenta
#define UCYAN    "\033[4m\033[36m"      // Cyan
#define UWHITE   "\033[4m\033[37m"      // White

// Background
#define BG_BLACK   "\033[40m"      // Black
#define BG_RED     "\033[41m"      // Red
#define BG_GREEN   "\033[42m"      // Green
#define BG_YELLOW  "\033[43m"      // Yellow
#define BG_BLUE    "\033[44m"      // Blue
#define BG_MAGENTA "\033[45m"      // Magenta
#define BG_CYAN    "\033[46m"      // Cyan
#define BG_WHITE   "\033[47m"      // White

#endif
